package com.hillel.calc;

public class MainCalc {
    public static int add (int x, int y) {
        int result = x + y;
        return result;
    }
    public static int sub (int x, int y) {
        int result = x - y;
        return result;
    }
    public static int mult (int x, int y) {
        int result = x * y;
        return result;
    }
}
